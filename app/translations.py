def _lookup(d, key):
    cur = d
    for part in key.split('.'):
        if not isinstance(cur, dict) or part not in cur:
            return None
        cur = cur[part]
    return cur


def t(key, lang=None, **kwargs):
    """Translate `key` (dotted path) into `lang` (defaults to g.lang)."""
    if lang is None:
        try:
            from flask import g
            lang = getattr(g, 'lang', DEFAULT_LANG)
        except RuntimeError:
            lang = DEFAULT_LANG

    value = _lookup(TRANSLATIONS.get(lang, {}), key)
    if value is None:
        value = _lookup(TRANSLATIONS[DEFAULT_LANG], key)
    if value is None:
        return key

    if isinstance(value, str) and kwargs:
        return value.format(**kwargs)
    return value


def format_price(value, lang=None):
    """Locale-aware price formatting: '40,00 €' (it) vs '€40.00' (en)."""
    if lang is None:
        try:
            from flask import g
            lang = getattr(g, 'lang', DEFAULT_LANG)
        except RuntimeError:
            lang = DEFAULT_LANG

    try:
        amount = float(value)
    except (TypeError, ValueError):
        return value

    formatted = f'{amount:,.2f}'
    if lang == 'it':
        formatted = formatted.replace(',', 'X').replace('.', ',').replace('X', '.')
        return f'{formatted} €'
    return f'€{formatted}'